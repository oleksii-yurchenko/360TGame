package org.yura.multi.processes.chat.common;

/**
 * The {@code ChatCommand} class contains a set of predefined commands
 * used in the chat application to perform specific actions such as
 * registering clients, acknowledging registration, and stopping the server.
 */
public class ChatCommand {
    public static final String CHAT_REGISTER = "CHAT_REGISTER";
    public static final String CHAT_REGISTER_ACK = "CHAT_REGISTER_ACK";
    public static final String CHAT_STOP_SERVER = "CHAT_STOP_SERVER";
}
